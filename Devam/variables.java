package Devam;

public class variables {
    public static void main(String[] args) {
        int a = 50;
        int b = 50;
        int sum = a + b;
        int diff = a - b;
        int mul = a * b;
        int div = a / b;
        System.out.println("The sum of two variables is: " + sum);
        System.out.println("The difference between two variables is: " + diff);
        System.out.println("The multiplication of two variables is: " + mul);
        System.out.println("The division of two variables is: " + div);
        // we use + symbol to differenciate variables with string of print statement
    }
}